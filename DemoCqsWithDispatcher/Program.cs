using DemoCqsWithDispatcher.Infrastructure;
using DemoCqsWithDispatcher.Models.Entities;
using Tools.Cqs;

namespace DemoCqsWithDispatcher
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();
            //FakeService de Contact
            builder.Services.AddSingleton<IList<Contact>>(sp => new  List<Contact>());

            builder.Services.AddHandlersAndDispatcher();
            

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
